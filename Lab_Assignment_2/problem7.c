#include <stdio.h>
#include <math.h>

int main()
{
    int i, j, k, y;
    int x = 64;

    printf("\n------------------------------------------------------------------------------------------------------------\n\n");

    for (i = 1; i <= 5; i++)
    {
        for (j = 1; j <= i; j++)
        {
            printf("%d ", i);
        }

        printf("\n");
    }

    printf("\n------------------------------------------------------------------------------------------------------------\n\n");

    for (i = 1; i <= 5; i++)
    {
        for (j = i; j <= 5; j++)
        {
            printf("  ");
        }
        for (j = 1; j <= i; j++)
        {
            printf("%d ", j);
        }
        printf("\n");
    }

    printf("\n------------------------------------------------------------------------------------------------------------\n\n");

    for (i = 1; i <= 5; i++)
    {
        for (j = 1; j <= i; j++)
        {
            printf("%c ", x + j);
        }
        printf("\n");
    }

    printf("\n------------------------------------------------------------------------------------------------------------\n\n");

    for (i = 1; i <= 5; i++)
    {
        for (j = i; j <= 5; j++)
        {
            printf("  ");
        }
        for (j = 1; j < i; j++)
        {
            printf("%d ", j);
        }
        for (j = i; j >= 1; j--)
        {
            printf("%d ", j);
        }

        printf("\n");
    }

    printf("\n------------------------------------------------------------------------------------------------------------\n\n");

    for (i = 1; i <= 6; i++)
    {
        y = 65;
        for (j = 1; j < i; j++)
        {
            printf("  ");
        }
        for (j = i; j < 6; j++)
        {

            printf("%c ", y++);
        }
        for (j = i; j <= 6; j++)
        {
            printf("%c ", y++);
        }
        printf("\n");
    }

    printf("\n------------------------------------------------------------------------------------------------------------\n\n");

    for (i = 1; i <= 4; i++)
    {

        for (j = i; j <= 4; j++)
        {
            printf("* ");
        }

        for (j = 1; j < i; j++)
        {

            printf("  ");
        }
        for (j = 1; j < i; j++)
        {

            printf("  ");
        }
        for (j = i; j <= 4; j++)
        {
            printf("* ");
        }

        printf("\n");
    }

    printf("\n------------------------------------------------------------------------------------------------------------\n\n");

    for (i = 1; i <= 6; i++)
    {

        for (j = 1; j <= 6; j++)
        {
            if (i == 1 || i == 6 || i + j == 7)
            {
                printf("* ");
            }
            else
            {
                printf("  ");
            }
        }

        printf("\n");
    }

    printf("\n------------------------------------------------------------------------------------------------------------\n\n");

    for (i = 1; i <= 5; i++)
    {

        for (j = 1; j <= 5; j++)
        {
            if (i == 1 || j == 1 || i == 5 || j == 5)
            {
                printf("*");
            }
            else
            {
                printf(" ");
            }
        }

        printf("\n");
    }

    return 0;
}